#include<stdio.h>
#include<stdlib.h>
main() {
	char id[][3] = {"10","11","12","13","14","15","16","17","34","18","19","20","21",
	"22","35","23","24","25","26","27","28","29","32","30","31","33"};
	char S[10];
	while(scanf("%s", S)==1) {
		int Sum = 0, a;
		for(a=0;a<8;a++)
			Sum += (S[a]-48)*(8-a);
		for(a=0;a<26;a++)
			if(10-(Sum + id[a][0]-48 + (id[a][1]-48)*9)%10 == S[8]-48)
				printf("%c",a+'A');
		puts("");
	}
	return 0;
}
