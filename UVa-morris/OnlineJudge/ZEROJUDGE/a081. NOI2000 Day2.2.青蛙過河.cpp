#include<stdio.h>
main() {
	int n, m, s2[26] = {1};
	for(n = 1; n < 26; n++)	s2[n] = s2[n-1]*2;
	while(scanf("%d %d", &n, &m) == 2) {
		printf("%d", s2[n]*(m+1));
	}
	return 0;
}
/*
設s[i,j]表示i根柱子，j片荷葉的情況下最多能通過的青蛙數。
不難得出：s[i,j]=2s[i-1,j]
解釋：i-1根柱子時，設終點為t，則有s[i-1,j]個青蛙到達t，增加1根柱子t'，
則到達t'的青蛙個數也為s[i- 1,j]，再將t'作為起點，則t'上的青蛙全部可以轉移到t上。
邊界條件，s[0,j]=j+1
所以這道題的答案就是2n(m+1)。
*/
